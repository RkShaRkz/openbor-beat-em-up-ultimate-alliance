void Portrait_Axel()
{//Spawn Hero portrait in select screen
	void vSpawn;
		
	clearspawnentry(); //Clear current spawn entry.
    setspawnentry("name", "Portrait_Axel"); //Acquire spawn entity by name.
	
	vSpawn = spawn();	
	
	changeentityproperty(vSpawn, "position", 130, 24, 0); //Set spawn location.
	changeentityproperty(vSpawn, "direction", 1); //Set spawn direction.
	
	return vSpawn; //Return vSpawn.


}

